#include <iostream>

class Shape {
public:
    virtual void draw() {
        std::cout << "V? m?t hình." << std::endl;
    }
};

class Circle : public Shape {
public:
    void draw() override {
        std::cout << "V? m?t hình tròn." << std::endl;
    }
};

int main() {
    Shape* hinh1 = new Shape();
    Shape* hinh2 = new Circle();

    hinh1->draw(); // V? m?t hình.
    hinh2->draw(); // V? m?t hình tròn.

    delete hinh1;
    delete hinh2;

    return 0;
}

